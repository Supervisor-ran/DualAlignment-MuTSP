import os

def root_path_to_tag(root_path: str, data_path: str) -> str:
    p = os.path.normpath(root_path)

    # leaf: Public_Health / Climate ...
    leaf = os.path.basename(p)

    # top: data / data_TTC / data_TimeCAP ...
    parts = p.split(os.sep)
    top = parts[0]
    if top == "." and len(parts) > 1:
        top = parts[1]

    # prefix: data_TTC -> TTC, data_TimeCAP -> TimeCAP, data -> TimeMMD
    if "_" in top:
        prefix = top.split("_", 1)[1]
    else:
        prefix = "TimeMMD"

    # special: Public_Health -> PublicHealth
    if leaf == "Public_Health":
        leaf = "PublicHealth"

    # ===== 新增：从 data_path 提取后缀 =====
    # 取文件名（防止 data_path 里带目录）
    fname = os.path.basename(data_path)
    stem, _ = os.path.splitext(fname)           # e.g. "US_VMT_Month"
    last_token = stem.split("_")[-1] if stem else ""

    # 取最后 3 个字符：Month -> nth, Day -> Day
    tail3 = last_token[-3:] if last_token else ""
    # 首字母大写：nth -> Nth, Day -> Day
    tail3 = tail3[:1].upper() + tail3[1:] if tail3 else ""

    # 拼接：PublicHealth + Day/Nth
    leaf = f"{leaf}{tail3}"

    return f"{prefix}_{leaf}"

